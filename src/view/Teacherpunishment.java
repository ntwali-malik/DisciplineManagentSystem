    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        // JDBC connection parameters
        String url = "jdbc:postgresql://localhost:5432/displine_management_system_db";
        String user = "postgres";
        String password = "123";

        // SQL SELECT query to retrieve data from the "punishment" table
        String selectSql = "SELECT * FROM punishment";

        try (Connection conn = DriverManager.getConnection(url, user, password);
                Statement statement = conn.createStatement()) {
            // Execute the SQL query
            ResultSet resultSet = statement.executeQuery(selectSql);

            // Create a new DefaultTableModel to hold the data
            DefaultTableModel model = new DefaultTableModel();

            // Add columns to the model
            model.addColumn("Punishment ID");
            model.addColumn("Student Names");
            model.addColumn("Reason");
            model.addColumn("Class Name");
            model.addColumn("Punishment");
            model.addColumn("Marks Deducted");

            // Populate the model with data from the ResultSet
            while (resultSet.next()) {
                Object[] rowData = {
                    resultSet.getInt("punishment_id"),
                    resultSet.getString("student_names"),
                    resultSet.getString("reason"),
                    resultSet.getString("class_name"),
                    resultSet.getString("punishment"),
                    resultSet.getInt("marks_deducted")
                };
                model.addRow(rowData);
            }

            // Set the model to the jTable
            punishmentStudentsTable.setModel(model);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_refreshBtnActionPerformed

