    private void punishExecuteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_punishExecuteBtnActionPerformed
        String studentNames = nameBox.getText();
        String reason = punishmentReasonBox.getText();
        String className = (String) classCombo.getSelectedItem();
        String punishment = punishDaysBox.getText();
        int marksDeducted = Integer.parseInt(marksDeductedBox.getText());

        // JDBC connection parameters
        String url = "jdbc:postgresql://localhost:5432/displine_management_system_db";
        String user = "postgres";
        String password = "123";

        // SQL INSERT statement to insert data into the "punishment" table
        String insertSql = "INSERT INTO punishment (student_names, reason, class_name, punishment, marks_deducted) VALUES (?, ?, ?, ?, ?)";

        try (Connection conn = DriverManager.getConnection(url, user, password);
                PreparedStatement statement = conn.prepareStatement(insertSql)) {
            // Set parameters for the prepared statement
            statement.setString(1, studentNames);
            statement.setString(2, reason);
            statement.setString(3, className);
            statement.setString(4, punishment);
            statement.setInt(5, marksDeducted);

            // Execute the INSERT query
            int rowsAffected = statement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Punishment Recorded successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearTextFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Record Punishment ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_punishExecuteBtnActionPerformed

